/** SSID Arabic metrics overrides — deep-merged with English metrics at i18n boot. */
import type { SsidMetricId } from '../types';

function band(metric: SsidMetricId, bandId: string, texts: {
  classification: string;
  meaning: string;
  physiology: string;
  performance: string;
  risk: string;
  reference: string;
  ai: string;
  immediate: string;
  weekly: string;
  longTerm: string;
}) {
  return {
    classifications: { [bandId]: texts.classification },
    meanings: { [bandId]: texts.meaning },
    physiology: { [bandId]: texts.physiology },
    performance: { [bandId]: texts.performance },
    risk: { [bandId]: texts.risk },
    reference: { [bandId]: texts.reference },
    ai: { [bandId]: texts.ai },
    recs: { [bandId]: { immediate: texts.immediate, weekly: texts.weekly, longTerm: texts.longTerm } },
  };
}

function mergeMetricParts(...parts: ReturnType<typeof band>[]) {
  type MetricPart = ReturnType<typeof band>;
  return parts.reduce(
    (acc, part) => ({
      classifications: { ...acc.classifications, ...part.classifications },
      meanings: { ...acc.meanings, ...part.meanings },
      physiology: { ...acc.physiology, ...part.physiology },
      performance: { ...acc.performance, ...part.performance },
      risk: { ...acc.risk, ...part.risk },
      reference: { ...acc.reference, ...part.reference },
      ai: { ...acc.ai, ...part.ai },
      recs: { ...acc.recs, ...part.recs },
    }),
    {
      classifications: {},
      meanings: {},
      physiology: {},
      performance: {},
      risk: {},
      reference: {},
      ai: {},
      recs: {},
    } as MetricPart
  );
}

